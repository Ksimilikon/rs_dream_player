//! общий стейт интерфейса и типы сообщений между TUI и оркестратором.

/// краткая информация о треке для отображения.
#[derive(Clone)]
pub struct TrackInfo {
    /// id трека в бд (нужен редактору плейлистов). -1 — трек вне бд.
    pub id: i64,
    pub title: String,
    pub artists: String,
    /// персональная громкость трека (0.0..=1.0).
    pub volume: f32,
    /// путь к обложке трека (из бд), если есть — показывается на вкладке SONG.
    pub cover: Option<String>,
    /// альбом (тег файла), если есть — показывается как `[альбом]` в списках.
    pub album: Option<String>,
    /// жанры (теги файла), показываются на вкладке SONG.
    pub genres: Vec<String>,
    /// цветовая метка (квадратик перед названием): red/pink/orange/green/blue/cyan/purple.
    pub color: Option<String>,
    /// текстовая метка пользователя (под обложкой на вкладке SONG).
    pub user_label: Option<String>,
    /// трек недействителен (файл не читается): красный в списках, пропускается.
    pub invalid: bool,
}

/// плейлист в списке (имя + его треки для предпросмотра на вкладке playlists).
#[derive(Clone)]
pub struct PlaylistEntry {
    pub name: String,
    pub tracks: Vec<TrackInfo>,
    /// виртуальный плейлист «весь пул песен» (грузится особым путём).
    pub pool: bool,
    /// сессионный временный плейлист: не сохранён в бд, помечается `[temp]`.
    pub temp: bool,
}

/// цель команды проверки наличия файлов (`:check`).
pub enum CheckTarget {
    /// весь индекс библиотеки.
    All,
    /// конкретный плейлист по имени.
    Playlist(String),
}

/// команды управления, которые TUI шлёт наружу (в оркестратор).
pub enum Control {
    Next,
    Prev,
    PlayPause,
    /// выбрать и проиграть трек по индексу в текущем плейлисте.
    Select(usize),
    /// загрузить плейлист по имени из бд и начать с трека `start`.
    LoadPlaylist { name: String, start: usize },
    /// загрузить виртуальный плейлист со всем пулом песен, начать с трека `start`.
    LoadPool { start: usize },
    /// громкость текущей песни (0.0..=1.0).
    SongVolume(f32),
    /// общая (мастер-) громкость (0.0..=1.0).
    MasterVolume(f32),
    /// сохранить плейлист в бд: имя + упорядоченные id треков.
    SavePlaylist { name: String, ids: Vec<i64> },
    /// собрать временный (несохраняемый) плейлист из id треков и проиграть с `start`.
    PlayTemp { ids: Vec<i64>, start: usize },
    /// задать заголовок трека (тег файла + бд).
    SetTitle { id: i64, title: String },
    /// задать список артистов трека (тег файла + бд).
    SetArtists { id: i64, artists: Vec<String> },
    /// задать альбом трека (тег файла + бд); пустая строка очищает.
    SetAlbum { id: i64, album: String },
    /// задать список жанров трека (тег файла + бд).
    SetGenres { id: i64, genres: Vec<String> },
    /// задать цветовую метку трека (бд); пустая строка очищает.
    SetColor { id: i64, color: String },
    /// задать текстовую метку трека (бд); пустая строка очищает.
    SetLabel { id: i64, label: String },
    /// переименовать файл трека на диске (в той же папке) + обновить путь в бд.
    RenameFile { id: i64, name: String },
    /// скопировать обложку в каталог конфига и сохранить путь в бд (приоритет).
    SetCover { id: i64, path: String },
    /// встроить обложку прямо в теги файла.
    SetCoverTag { id: i64, path: String },
    /// присвоить недействительному треку новый путь к файлу.
    SetPath { id: i64, path: String },
    /// удалить трек из индекса (каскадом из всех плейлистов).
    RemoveTrack(i64),
    /// удалить из индекса все недействительные треки.
    PurgeInvalid,
    /// проиндексировать заданный каталог в бд.
    Scan(String),
    /// проверить наличие файлов треков; отсутствующие помечаются недействительными.
    Check(CheckTarget),
}

/// обновления состояния, которые оркестратор шлёт в TUI.
pub enum Update {
    /// индекс текущего трека сменился.
    NowPlaying(usize),
    /// загружен новый плейлист — список треков и его имя.
    Playlist { name: String, tracks: Vec<TrackInfo> },
    /// обновился набор плейлистов из бд (после сохранения нового).
    Playlists(Vec<PlaylistEntry>),
    /// метаданные трека изменились — новая инфа применяется на месте ко всем
    /// совпадениям по id (без переключения вкладки и сброса позиции).
    TrackPatch(TrackInfo),
    /// разовое сообщение уровня «ошибка/итог» (красное в статус-строке).
    Error(String),
    /// разовое информационное сообщение (жёлтое в статус-строке).
    Notice(String),
}

/// общий стейт, который читают вкладки при отрисовке.
pub struct Model {
    /// все плейлисты с их треками (для вкладки playlists и предпросмотра).
    pub playlists: Vec<PlaylistEntry>,
    /// имя текущего (играющего) плейлиста ("---" для анонимного).
    pub playlist_name: String,
    /// треки текущего (играющего) плейлиста.
    pub tracks: Vec<TrackInfo>,
    /// индекс играющего трека.
    pub current: usize,
    /// общая громкость 0.0..=1.0.
    pub master_vol: f32,
    /// текст конфига для вкладки настроек (готовый к показу).
    pub config_text: String,
}

impl Model {
    /// громкость текущей песни (или 1.0, если трека нет).
    pub fn song_vol(&self) -> f32 {
        self.tracks.get(self.current).map(|t| t.volume).unwrap_or(1.0)
    }
}
